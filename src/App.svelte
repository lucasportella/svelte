<script>
	 let name = 'Lucas';
	 const handleInput = (e) => {
		name = e.target.value
	 }
	 const handleClick = () => {
		name = 'new title'
	 }
</script>

<main>
	<h1>Hello {name}!</h1>
	<p>Visit the <a href="https://svelte.dev/tutorial">Svelte tutorial</a> to learn how to build Svelte apps.</p>
	<button on:click={handleClick}>update title</button>
	<input on:input={handleInput} bind:value={name}/>
</main>

